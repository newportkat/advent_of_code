#include <stdio.h>

int calculateFloor(const char *instructions) {
  int floor = 0;

  for (int i = 0; instructions[i] != '\0'; i++) {
    if (instructions[i] == '(') {
      floor++;
    } else if (instructions[i] == ')') {
      floor--;
    }
  }

  return floor;
}

