#include <stdio.h>
#include <string.h>

void calculateBasementIndex(const char *instructions) {
  int floor = 0;

  for (int i = 0; i < strlen(instructions); i++) {
    if (instructions[i] == '(') {
      floor++;
      if (floor == -1) {
        printf("The position of the character is: %d\n", i + 1);
        break;
      }
    } else if (instructions[i] == ')') {
      floor--;
      if (floor == -1) {
        printf("The position of the character is: %d\n", i + 1);
        break;
      }
    }
  }
}

